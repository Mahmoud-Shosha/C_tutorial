#include <stdio.h>
#include <stdbool.h>

int main(void)
{
    printf("6 & 14 ==> %d\n", 6 & 14); // 0110 & 1110 ==> 0110
    printf("6 | 14 ==> %d\n", 6 | 14); // 0110 | 1110 ==> 1110
    printf("6 ^ 14 ==> %d\n", 6 ^ 14); // 0110 ^ 1110 ==> 1000
    printf("~14 ==> %d\n", ~14);       // ~00001110 ==> 11110001

    printf("5 << 2 ==> %d\n", 5 << 2); // 0000 0101 << 2 ==> 0001 0100
    printf("7 >> 2 ==> %d\n", 7 >> 2); // 0111 >> 2 ==> 0001

    return 0;
}